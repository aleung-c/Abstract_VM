#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	unsigned int i;
	unsigned int x;

	i = 0;
	x = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[x] != '\0')
	{
		s1[i] = s2[x];
		x++;
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
