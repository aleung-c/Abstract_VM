#include "libft.h"

void	ft_strclr(char *s)
{
	int len;
	int i;

	if (s)
	{
		i = 0;
		len = ft_strlen(s);
		while (i < len)
		{
			*s = '\0';
			s++;
			i++;
		}
	}
}
