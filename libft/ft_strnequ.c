#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while (n - 1)
	{
		if (s1[n - 1] != s2[n - 1])
			return (0);
		n--;
	}
	if (s2[n - 1] != s1[n - 1])
		return (0);
	return (1);
}
